export function getBmCargoBranchAddress(branchName = "") {
  const directAddress = bmCargoBranchAddresses[branchName];
  if (directAddress) return directAddress;

  const withoutProvince = branchName.endsWith(")") && branchName.includes(" (")
    ? branchName.split(" (").slice(0, -1).join(" (").trim()
    : branchName;

  return bmCargoBranchAddresses[withoutProvince] || "";
}

